r = float(input("Introduce el radio del círculo: "))

diametro = 2 * r
circunferencia = 3.1416 * diametro
area = 3.1416 * r * r
volumen = (4/3) * 3.1416 * r * r * r

print("El diámetro es:", diametro)
print("La circunferencia es:", circunferencia)
print("El área es:", area)
print("El volumen es:", volumen)
